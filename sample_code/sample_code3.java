package sample_code;

public class sample_code3 {
    public static void main(String[] args) {
        int n = 10;

        switch(n) {
            case 10:
                System.out.println("n is 10");
                break;
            case 20:
                System.out.println("n is 20");
                break;
            case 30:
                System.out.println("n is 30");
                break;
            default:
                System.out.println("n is not 10, 20, or 30");
        }
    }
}
